package clubfs_mfo;

/**
 *
 * @author thang
 */
public class City {
        private int name;
	private double x;
	private double y;

    public City(int name, double x, double y) {
        this.name = name;
        this.x = x;
        this.y = y;
    }

    public int getName() {
        return name;
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }

    

	

	
}
